package com.itheima.comment;

public class Comment {
    static void main(String[] args) {
        //打印两行诗句
        System.out.println("HelloWorld!");
        /*
        多行注释
        打印三行诗句
         */
        System.out.println("HelloWorld!");
        System.out.println("床前明月光，疑是地上霜");
        System.out.println("HelloWorld!");

    }
}

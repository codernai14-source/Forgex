package com.itheima.operator;

public class OperatorDemo4 {
    static void main(String[] args) {
        print(3,9);
        print(3,3);

    }
    public static void print(int a,int b) {
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
